         <pre>
    Most people relate Electrical Engineering with wires and circuits but it is much more than that.
Electrical Engineers are needed not only in construction or power industry but also in telecommunication,
IT, navigation, railways, automobile, architecture, aerospace, defence, etc. From maintaining power grids 
to supervising research and development of electronic devices and technology to designing circuits for
computers, Electrical Engineers are required in various fields.
   This branch of Engineering is basically required broadly in two fields: Power and Telecommunication.
Power Engineers: Power Engineers are involved in generation and transmission of electricity. Their 
service is sought the most in automobile industry as they are responsible for designing, manufacturing 
and maintaining engines and maintaining the flow of power from the engine attached to devices. 
Power Engineers having specialized skills in microelectronics design and manufacture very small power 
circuits used in computers and other electronic devices. The average starting salary of Power Engineers
 is Rs 4 lakh and upwards.

Telecommunication Engineers: They mainly work on designing and maintenance of cables. Their service is
also sought for production of receivers and transceivers. The average starting salary of Telecommunication 
Engineer is Rs 4 lakh and upwards. Key roles of an Electrical Engineer are:

Designing, manufacturing and operating power plants, industrial machinery, electrical motors, 
and ignition systems for automobiles, aircrafts, space crafts and other types of engines

Research and design improved ways for using electrical power

Compute the cost of manufacture, construction and installation of electrical equipment as per specifications
